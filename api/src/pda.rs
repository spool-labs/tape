#[cfg(debug_assertions)]
pub fn system_pda() -> (Pubkey, u8) {
    Pubkey::find_program_address(&[SYSTEM], &crate::id())
}

#[cfg(not(debug_assertions))]
#[inline(always)]
pub fn system_pda() -> (Pubkey, u8) {
    (SYSTEM_ADDRESS, SYSTEM_BUMP)
}

