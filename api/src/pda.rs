#[cfg(debug_assertions)]
pub fn treasury_ata() -> (Pubkey, u8) {
    let (treasury_pda, _bump) = treasury_pda();
    let (mint_pda, _bump) = mint_pda();
    Pubkey::find_program_address(
        &[
            treasury_pda.as_ref(),
            spl_token::ID.as_ref(),
            mint_pda.as_ref(),
        ],
        &spl_associated_token_account::ID,
    )
}

